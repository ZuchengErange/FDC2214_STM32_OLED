//按键初始化函数 
//PB5,6,7,8 设置成输入
void KEY_Init(void)
{
	
	GPIO_InitTypeDef GPIO_InitStructure;

 	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB,ENABLE);//使能PORTA,PORTC时钟

	GPIO_PinRemapConfig(GPIO_Remap_SWJ_JTAGDisable, ENABLE);//关闭jtag，使能SWD，可以用SWD模式调试
	
	GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_11|GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15;//PB11,13,14,15
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPU; //设置成上拉输入
 	GPIO_Init(GPIOB, &GPIO_InitStructure);//初始化GPIOAB
	
	
//	GPIO_InitStructure.GPIO_Pin  = GPIO_Pin_0;//PA0
//	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; //PA0设置成输入，默认下拉	  这个是指唤醒按键 
//	GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.0
	
} 
//按键处理函数
//返回按键值
//mode:0,不支持连续按;1,支持连续按;
//返回值：
//0，没有任何按键按下
//KEY0_PRES，KEY0按下
//KEY1_PRES，KEY1按下
//WKUP_PRES，WK_UP按下 
//注意此函数有响应优先级,KEY0>KEY1>WK_UP!!
u8 KEY_Scan(u8 mode)
{ 
	static u8 key_up=1;//按键按松开标志
	if(mode)key_up=1;  //支持连按		  
	if((key_up)&&((KEY1==0)||(KEY2==0)||(KEY3==0)||(KEY4==0)))
	{
		delay_ms(10);//去抖动 
		key_up=0;
		if(KEY1==0)      return KEY1_PRES;
		else if(KEY2==0) return KEY2_PRES;
		else if(KEY3==0) return KEY3_PRES;
		else if(KEY4==0) return KEY4_PRES;
	}
	//	else if(WK_UP==1)return WKUP_PRES; 
	else if((KEY1==1)&&(KEY2==1)&&(KEY3==1)&&(KEY4==1))key_up=1; 	     
	return 0; 
	// 无按键按下
}
